/**
 * Convert regular text to styled unicode text.
 * 
 * @param text - Input text to style
 * @param style - Style to apply (must be a key in STYLE_CONFIG)
 * @returns Text with the specified style applied
 */
export function styleText(text: string, style: string): string {
  if (!(style in STYLE_CONFIG)) {
    return text;
  }
  
  let result = "";
  for (const char of text) {
    if (char in STYLE_CONFIG[style]) {
      result += STYLE_CONFIG[style][char];
    } else {
      result += char;
    }
  }
  return result;
}

/**
 * Add specified combining diacritical marks to each character in text.
 * 
 * @param text - Input text
 * @param marks - Array of combining marks to add
 * @returns Text with diacritical marks added
 */
export function addDiacritics(text: string, marks: string[]): string {
  let result = "";
  for (const char of text) {
    let markedChar = char;
    for (const mark of marks) {
      markedChar += mark;
    }
    result += markedChar;
  }
  return result;
}

/**
 * Get all available text styles
 * @returns Array of available style names
 */
export function getAvailableStyles(): string[] {
  return Object.keys(STYLE_CONFIG);
}

/**
 * Get display name for a style
 * @param style - Style key
 * @returns Formatted display name
 */
export function getStyleDisplayName(style: string): string {
  return style.replace(/_/g, ' ').replace(/\b\w/g, l => l.toUpperCase());
}

/**
 * Get a preview of what a style looks like
 * @param style - Style key
 * @returns Sample text in the given style
 */
export function getStylePreview(style: string): string {
  return styleText('Sample', style);
}

